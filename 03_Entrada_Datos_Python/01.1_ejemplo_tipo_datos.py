'''     EJEMPLOS DE CONVERSION DE TIPOS DE DATOS    '''

# Concatenación o Suma de Strings
'''
    - Si se suman dos Strings o más, estos se alinearan uno al lado del otro (sin espacios).
'''
numero_string_1 = '10'
numero_string_2 = '20'
print(f'Número String 1: {numero_string_1}')
print(f'Número String 2: {numero_string_2}\n')
resultado = numero_string_1 + numero_string_2
print(f'Concatenación de Strings: {resultado}\n')

# Conversión de String a Número
numero_entero_1 = int(numero_string_1)
numero_entero_2 = int(numero_string_2)
resultado = numero_entero_1 + numero_entero_2
print(f'Suma de Enteros: {resultado}')