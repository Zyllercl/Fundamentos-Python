
# Iteradores

"""
    Que es un iterador? -> Se desea correr el mismo código determinado número de ocasiones hasta que una condición se cumpla (o se deja de cumplir)
"""

# Variables
lenguajes = ['Python', 'Kotlin', 'Java', 'JavaScript', 'Bash']

# Iterador
for lenguaje in lenguajes:
    print(f'Iterando el arreglo: {lenguaje}')

# Iterador que escriba numeros
for numero in range(0, 5):
    print(numero)