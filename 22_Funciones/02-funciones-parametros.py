
# Funciones con Parametros y Argumentos

# Funcion con Parametros
def informacion(nombre, cargo = 'Undefined'):
    print(f'Bienvenido {nombre} y soy {cargo}')

# Llamar a una funcion con Argumentos
informacion('Martin', 'Programador Python')
informacion('Miguel', 'Programador Web')
informacion('Kevin', 'Programador Laravel')