'''
    COLECCIONES EN PYTHON (LISTAS - TUPLAS - DICCIONARIOS)

    Def:
        - Una colección es un conjunto de datos. En Python existen varios tipos que podemos utilizar con el objetivo de almacenar, organizar y manipular múltiples conjuntos de datos, esto se conoce como Estructura de Datos.

        - Tipos de Estructuras de Datos:
        
            1.- Listas
                Def:
                    - Las listas son colecciones ordenadas y mutables de elementos que pueden ser de diferente tipo. Las listas son dinámicas, es decir, que pueden cambiar de tamaño, podemos añadir, modificar o eliminar elementos,

                    Ejemeplo:
                        - Sintaxis listas
                            lista = [elemento1, elemento2, elemento3]
                    
            2.- Tuplas
                Def:
                    - Las tuplas son similares a las listas, son una colección de datos ordenados, pero las tuplas son inmutables, es decir, una vez creada una tupla no es posible modificar su tamaño, ni agregar más elementos, ni modificarlos ni eliminarlos.

                    - Las tuplas suelen utilizarse para crear colecciones de datos que no deben cambiar en el tiempo.

                        Ejemplo:
                            - Sintaxis tuplas
                                tupla = (elemento1, elemento2, elemento3)
                                tupla_sin_parentesis = elemento1, elemento2, elemento3

            3.- Set (conjunto)
                Def:
                    - Los sets (conjuntos) son colecciones de datos no ordenados de elementos únicos. Son muy útiles cuando debemos asegurarnos de que no hayan elementos duplicados en una coleccion (lista, tupla, etc). 

                    - Algunas caracteristicas:
                        -> No mantiene el orden de los elementos
                        -> No se puede modificar los elementos ya creados en un set
                        -> Se pueden agregar y eliminar elementos
            
                    Ejemplo:
                        - Sintaxis set
                            set = {elemento1, elemento2, elemento3}
            
            4.- Diccionario
                Def:
                    - Los diccionarios son una colección de datos ordenada y se almacenan en forma de " llave:valor ". Permite almacenar cualquier tipo de datos, pero para acceder a estos elementos se debe utilizar su indice, es decir, la "llave" para acceder al "valor".

                    Ejemplo:
                        - Sintaxis diccionario
                            diccionario = {clave1: valor1, clave2: valor2}
'''
