'''
    FUNCIONES EN PYTHON

    Def:
        - Las funciones en Python son bloques de código para relizar una tarea e particular. Las funciones se pueden reutilizar en diferentes partes de un programa.

        - Para poder mandar a llamar a una función, es necesario declararla primero y luego llamarla, si se hace a la inversa, dara un error de que la función aun no se ha declarado.

        Ventajas de las Funciones:

            1.- Modularidad:    Las funciones permiten dividir un programa en partes más pequeñas y manejables. Cada función puede ser desarrollada por separado e incluso por distintos programadores, creando al final programas mas comolejos.

            2.- Reutilización de código:    Una vez creada la función, se puede utilizar tantas veces sea necesaria. Esto evita duplicación de código y minimiza los errores.

            3.- Manteminiemto:  Modificar un programa que usa funciones es más fácil, ya que podemos localizar los errores más rapido y corregirlos. Esto reduce el riesgo de efectos no deseados en otras partes del programa.

            4.- Parametrización:    Las funciones pueden ser diseñadas para aceptar parámetros lo que hace más flexibles los programas.

            5.- Colaboración:   En proyectos grandes, el uso de mpodulos (archivos con multiples funciones) se hace imprescindible al colaborar con varios programadores.
'''