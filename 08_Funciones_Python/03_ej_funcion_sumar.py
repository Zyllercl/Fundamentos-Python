print(f'-----   FUNCION SUMAR   -----')

# Definicion de la funcion
def sumar(a, b):
    resultado = a + b
    return resultado

# Llamar a la funcion
resultado_funcion = sumar(5, 7)
print(f'Resultado de la suma: {resultado_funcion}')

resultado_funcion = sumar(5, 75)
print(f'Resultado de la suma: {resultado_funcion}')