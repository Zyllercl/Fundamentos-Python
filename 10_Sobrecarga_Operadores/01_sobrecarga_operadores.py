"""
Sobrecarga de Operadores en Python
==================================
Este script muestra ejemplos básicos de como ocupar la sobrecarga de operadores.
"""

# -----------------------
# Operador +
# -----------------------
print('[OPERADOR +]')

# Variables Númericas
a = 1
b = 3
print(f'Suma númerica: {a + b}')

# Variables Strings
a = 'Hola '
b = 'Mundo'
print(f'Suma strings: {a + b}')

# Variables Listas
a = [1,2,3]
b = [5,6,7]
print(f'Suma listas: {a + b}')

