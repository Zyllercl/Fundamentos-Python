
# Funciones con numeros

# Funciones
def sumar(a, b):
    suma = a + b
    print(f'La suma es de: {suma}')

sumar(2, 3)

def restar(a, b):
    resta = a - b
    print(f'La resta es de: {resta}')

restar(5, 2)

def multiplicacion(a, b):
    multiplicar = a * b
    print(f'La multiplicacion es de: {multiplicar}')

multiplicacion(5, 2)

def division(a, b):
    dividir = a / b
    print(f'La multiplicacion es de: {dividir}')

division(5, 2)