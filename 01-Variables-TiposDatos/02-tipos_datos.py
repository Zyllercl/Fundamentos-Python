
# Tipos Datos

"""
    Diferencia de tipos de datos

    String      -> Cadena de texto
    Integers    -> Numeros enteros
    Float       -> Numeros flotantes
    Boolean     -> Booleano (True - False)

    Diferencia de tipados

    Tipado Estático (Static Typed Languages) -> Se agrega un tipo de dato a una variable y solo se puede recibir valores de ese tipo.
        # Ejemplo (Java o C#)
            int edad = 20 -> La variable edad solo podra recibir valores numericos

    Tipado Dinámico (Dynamically Typed Languages) -> Una variable puede contener cualquier tipo de dato y re asignarse diferentes valores.
        # Ejemplo (JavaScript, Ruby, Python o PHP)
            edad = 20
            edad = "Python"
"""

# String
nombre = "Elmo"
print(nombre)
# Integer
edad = 26
print(edad)
# Float
total_a_pagar = 4.5
print(total_a_pagar)
# Boolean
pagado = True
print(pagado)
