'''
    PROGRAMACIÓN ORIENTADA A OBJETOS (POO)

    Def:
        - Python es un lenguaje Orientado a Objetos. Un objeto es una representación de una entidad de la vida real en nuestro programa, es decir, que para crear un objeto en Python primero necesitamos crear una Clase (Class).

        - Una clase representa las características en común de nuestros objetos, es decir, es una abstracción.

    Clases y Objetos:
        - Si necesitamos construir un edificio, primero se necesita su plano, es decir, una 'Clase' o plantilla, y a partir de ese plano se puede crear muchos edificios similares, es decir, los 'objetos'.
    
    Elementos de una Clase:
        - Una clase se compone de atributos y métodos:

            Atributos:  Los atributos son las características de nuestros objetos.

            Métodos:    Los métodos son las acciones que pueden realizar nuestros objetos. Estas acciones son funciones, pero cuando se asocian con una 'Clase' se les denomina 'métodos'.

        - Una vez se ha definido una 'Clase', se pueden crear objetos, a esto se le llama 'instancias una clase'.

            Ejemplo:

                    Persona         -> Nomble de la Clase
                --------------
                nombre
                apellido            -> Atributos
                email
                celular
                --------------
                agregar Nombre   ()  -> Métodos
                mostrar Apellido ()

                Objeto 1:               Objeto 2:
                nombre = Jhon           nombre = Bellamy
                apellido = Reyes        apellido = Blake
                .                       .
                .                       .
                .                       .
                
'''