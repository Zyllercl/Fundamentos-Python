"""
Uso de Operador Ternario en Python
==================================
Este script muestra un ejemplo de cómo utilizar el operador ternario en Python para asignar valores basados en una condición de manera concisa.
"""

# -----------------------------
# Ejemplo de operador ternario en Python
# -----------------------------
edad = 21

es_adulto = "Si" if edad >= 18 else "No" 
# Si la condición es Verdadera, se ejecutará lo que se encuentra a la izquierda de la condición, de lo contrario se ejecutará lo que se encuentra a la derecha de la condición.

print(f'Es mayor de edad: {es_adulto}')
