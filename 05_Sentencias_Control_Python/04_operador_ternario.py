'''
    OPERADOR TERNARIO

    Def:
        - El operador ternario en Python es una forma compacta de agregar una condición, y el objetivo es asignar un valor a una variable dependiendo del valor de la condición.
            Ejemplo:
                - Sintaxis Operador Ternario:
                    resultado = valor_si_verdadero if condicion else valor_si_falso
'''

'''     USO DE OPERADOR TERNARIO   '''
# Ejemplo Operador Ternario
edad = 21
es_adulto = "Si" if edad >= 18 else "No" # Si la condición es Verdadera, se ejecutará lo que se encuentra a la izquierda de la condición, de lo contrario se ejecutará lo que se encuentra a la derecha de la condición.

print(f'Es mayor de edad: {es_adulto}')
