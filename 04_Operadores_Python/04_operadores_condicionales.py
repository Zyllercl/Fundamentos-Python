# ----------------------------------
# Variables Globales
# ----------------------------------
a, b = 7, 2
print(f'Variables Iniciales:\n a: {a}\n b: {b}\n')

# ------------------------------
# Ejemplo de Operador de Igualdad ( == )
# ------------------------------
print(f'-----   Operador de Igualdad   -----')
resultado = (a == b)
print(f'Resultado a == b es: {resultado}\n')

# ------------------------------
# Ejemplo de Operador Distinto ( != )
# ------------------------------
print(f'-----   Operador Distinto   -----')
resultado = (a != b)
print(f'Resultado a != b es: {resultado}\n')

# ------------------------------
# Ejemplo de Operador Menor que ( < )
# ------------------------------
print(f'-----   Operador Menor que   -----')
resultado = (a < b)
print(f'Resultado a < b es: {resultado}\n')

# ------------------------------
# Ejemplo de Operador Menor o igual que ( <= )
# ------------------------------
print(f'-----   Operador Menor o Igual que   -----')
resultado = (a <= b)
print(f'Resultado a <= b es: {resultado}\n')

# ------------------------------
# Ejemplo de Operador Mayor que ( > )
# ------------------------------
print(f'-----   Operador Mayor que   -----')
resultado = (a > b)
print(f'Resultado a > b es: {resultado}\n')

# ------------------------------
# Ejemplo de Operador Mayor o igual que ( >= )
# ------------------------------
print(f'-----   Operador Mayor o Igual que   -----')
resultado = (a >= b)
print(f'Resultado a >= b es: {resultado}\n')