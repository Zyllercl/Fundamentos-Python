'''
    OPERADORES EN PYTHON

    Def:
        - Los operadores son símbolos especiales que están diseñados para realizar operaciones específicas como:
            
            - Operadores Aritméticos:       Permiten realizar cálculos matemáticos básicos como suma, resta, multiplicación y división.
            - Operadores de Asignación:     Se utilizan para asignar valores a una variable.
            - Operadores de Comparación:    Se utilizan para comprar un valor con otro.
            - Operadores Lógicos:           Se utilizan para combinar expresiones o condicionales o lógicas.
            - Operadores de Identidad:      Se utilizan para comparar si dos variables son el mismo objeto
            - Operadores de Membresia:      Se utilizan para probar si una secuencia se presenta en un objeto (Ej: subcadena).
'''

# Operador Suma
print(2+3)
# Operador Resta
print(4-1)
# Operador Multiplicador
print(4*2)
# Operador Division
print(10/2)
# Operador Potencia
print( 3 ** 3)

print( (2+3) * 3 )

numero = 10
print(f'Numero: {numero}')
numero += 1
print(f'Numero incrementado: {numero}')